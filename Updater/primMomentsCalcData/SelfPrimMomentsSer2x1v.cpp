void SelfPrimMoments2x1vSer_P1(const int pVdim, const double *m0, const double *m1, const double *m2, const double *cM, const double *cE, double *u, double *vtSq) 
{ 
  // m0,m1,m2: moments of the distribution function. 
  // cM, cE: vtSq*cM and vtSq*cE are corrections to u and vtSq, respectively. 
  // u:        velocity. 
  // vtSq:     squared thermal speed, sqrt(T/m). 
 
  // If a corner value is below zero, use cell average m0.
  bool m0Avg = false;
  if (1.5*m0[3]-0.8660254037844386*m0[2]-0.8660254037844386*m0[1]+0.5*m0[0] < 0) { 
    m0Avg = true;
  }
  if (1.5*m0[3]-0.8660254037844386*m0[2]-0.8660254037844386*m0[1]+0.5*m0[0] < 0) { 
    m0Avg = true;
  }
  if ((-1.5*m0[3])-0.8660254037844386*m0[2]+0.8660254037844386*m0[1]+0.5*m0[0] < 0) { 
    m0Avg = true;
  }
  if ((-1.5*m0[3])-0.8660254037844386*m0[2]+0.8660254037844386*m0[1]+0.5*m0[0] < 0) { 
    m0Avg = true;
  }
 
  double m0s[4]; 
  double m1s[4]; 
  double m2s[4]; 
  if (m0Avg) { 
    m0s[0] = m0[0]; 
    m0s[1] = 0.0; 
    m0s[2] = 0.0; 
    m0s[3] = 0.0; 
    m1s[0] = m1[0]; 
    m1s[1] = 0.0; 
    m1s[2] = 0.0; 
    m1s[3] = 0.0; 
    m2s[0] = m2[0]; 
    m2s[1] = 0.0; 
    m2s[2] = 0.0; 
    m2s[3] = 0.0; 
  } else { 
    m0s[0] = m0[0]; 
    m0s[1] = m0[1]; 
    m0s[2] = m0[2]; 
    m0s[3] = m0[3]; 
    m1s[0] = m1[0]; 
    m1s[1] = m1[1]; 
    m1s[2] = m1[2]; 
    m1s[3] = m1[3]; 
    m2s[0] = m2[0]; 
    m2s[1] = m2[1]; 
    m2s[2] = m2[2]; 
    m2s[3] = m2[3]; 
  } 
 
  // Declare Eigen matrix and vectors for weak division. 
  Eigen::MatrixXd BigAEM = Eigen::MatrixXd::Zero(8,8); 
  Eigen::VectorXd bEV = Eigen::VectorXd::Zero(8);  
  Eigen::VectorXd xEV = Eigen::VectorXd::Zero(8);  
 
  // ....... Block from weak multiply of uX and m0  .......... // 
  BigAEM(0,0) = 0.5*m0s[0]; 
  BigAEM(0,1) = 0.5*m0s[1]; 
  BigAEM(0,2) = 0.5*m0s[2]; 
  BigAEM(0,3) = 0.5*m0s[3]; 
  BigAEM(1,0) = 0.5*m0s[1]; 
  BigAEM(1,1) = 0.5*m0s[0]; 
  BigAEM(1,2) = 0.5*m0s[3]; 
  BigAEM(1,3) = 0.5*m0s[2]; 
  BigAEM(2,0) = 0.5*m0s[2]; 
  BigAEM(2,1) = 0.5*m0s[3]; 
  BigAEM(2,2) = 0.5*m0s[0]; 
  BigAEM(2,3) = 0.5*m0s[1]; 
  BigAEM(3,0) = 0.5*m0s[3]; 
  BigAEM(3,1) = 0.5*m0s[2]; 
  BigAEM(3,2) = 0.5*m0s[1]; 
  BigAEM(3,3) = 0.5*m0s[0]; 
 
  // ....... Block from correction to uX .......... // 
  BigAEM(0,4) = -0.5*cM[0]; 
  BigAEM(0,5) = -0.5*cM[1]; 
  BigAEM(0,6) = -0.5*cM[2]; 
  BigAEM(0,7) = -0.5*cM[3]; 
  BigAEM(1,4) = -0.5*cM[1]; 
  BigAEM(1,5) = -0.5*cM[0]; 
  BigAEM(1,6) = -0.5*cM[3]; 
  BigAEM(1,7) = -0.5*cM[2]; 
  BigAEM(2,4) = -0.5*cM[2]; 
  BigAEM(2,5) = -0.5*cM[3]; 
  BigAEM(2,6) = -0.5*cM[0]; 
  BigAEM(2,7) = -0.5*cM[1]; 
  BigAEM(3,4) = -0.5*cM[3]; 
  BigAEM(3,5) = -0.5*cM[2]; 
  BigAEM(3,6) = -0.5*cM[1]; 
  BigAEM(3,7) = -0.5*cM[0]; 
 
  // ....... Block from weak multiply of uX and m1X  .......... // 
  BigAEM(4,0) = 0.5*m1s[0]; 
  BigAEM(4,1) = 0.5*m1s[1]; 
  BigAEM(4,2) = 0.5*m1s[2]; 
  BigAEM(4,3) = 0.5*m1s[3]; 
  BigAEM(5,0) = 0.5*m1s[1]; 
  BigAEM(5,1) = 0.5*m1s[0]; 
  BigAEM(5,2) = 0.5*m1s[3]; 
  BigAEM(5,3) = 0.5*m1s[2]; 
  BigAEM(6,0) = 0.5*m1s[2]; 
  BigAEM(6,1) = 0.5*m1s[3]; 
  BigAEM(6,2) = 0.5*m1s[0]; 
  BigAEM(6,3) = 0.5*m1s[1]; 
  BigAEM(7,0) = 0.5*m1s[3]; 
  BigAEM(7,1) = 0.5*m1s[2]; 
  BigAEM(7,2) = 0.5*m1s[1]; 
  BigAEM(7,3) = 0.5*m1s[0]; 
 
  // ....... Block from correction to vtSq .......... // 
  BigAEM(4,4) = 0.5*m0s[0]*pVdim-0.5*cE[0]; 
  BigAEM(4,5) = 0.5*m0s[1]*pVdim-0.5*cE[1]; 
  BigAEM(4,6) = 0.5*m0s[2]*pVdim-0.5*cE[2]; 
  BigAEM(4,7) = 0.5*m0s[3]*pVdim-0.5*cE[3]; 
  BigAEM(5,4) = 0.5*m0s[1]*pVdim-0.5*cE[1]; 
  BigAEM(5,5) = 0.5*m0s[0]*pVdim-0.5*cE[0]; 
  BigAEM(5,6) = 0.5*m0s[3]*pVdim-0.5*cE[3]; 
  BigAEM(5,7) = 0.5*m0s[2]*pVdim-0.5*cE[2]; 
  BigAEM(6,4) = 0.5*m0s[2]*pVdim-0.5*cE[2]; 
  BigAEM(6,5) = 0.5*m0s[3]*pVdim-0.5*cE[3]; 
  BigAEM(6,6) = 0.5*m0s[0]*pVdim-0.5*cE[0]; 
  BigAEM(6,7) = 0.5*m0s[1]*pVdim-0.5*cE[1]; 
  BigAEM(7,4) = 0.5*m0s[3]*pVdim-0.5*cE[3]; 
  BigAEM(7,5) = 0.5*m0s[2]*pVdim-0.5*cE[2]; 
  BigAEM(7,6) = 0.5*m0s[1]*pVdim-0.5*cE[1]; 
  BigAEM(7,7) = 0.5*m0s[0]*pVdim-0.5*cE[0]; 
 
  // ....... RHS vector is composed of m1 and m2 .......... // 
  bEV << m1s[0],m1s[1],m1s[2],m1s[3],m2s[0],m2s[1],m2s[2],m2s[3]; 
 
  xEV = BigAEM.colPivHouseholderQr().solve(bEV); 
 
  Eigen::Map<VectorXd>(u,4,1) = xEV.segment<4>(0); 
 
  Eigen::Map<VectorXd>(vtSq,4,1) = xEV.segment<4>(4); 
 
} 
 

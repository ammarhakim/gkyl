  data->AEM_S(8,9) = 0.5*m0Sr[1]-0.5*cE[1]; 
  data->AEM_S(8,10) = 0.5*m0Sr[2]-0.5*cE[2]; 
  data->AEM_S(8,11) = 0.5*m0Sr[3]-0.5*cE[3]; 
  data->AEM_S(9,8) = 0.5*m0Sr[1]-0.5*cE[1]; 
